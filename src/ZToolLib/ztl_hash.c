#include "ztl_hash.h"


uint32_t ztl_murmur_hash2(unsigned char* data, uint32_t len)
{
    unsigned int h, k;

    h = 0 ^ len;

    while (len >= 4) {
        k = data[0];
        k |= data[1] << 8;
        k |= data[2] << 16;
        k |= data[3] << 24;

        k *= 0x5bd1e995;
        k ^= k >> 24;
        k *= 0x5bd1e995;

        h *= 0x5bd1e995;
        h ^= k;

        data += 4;
        len -= 4;
    }

    switch (len) {
    case 3:
        h ^= data[2] << 16;
    case 2:
        h ^= data[1] << 8;
    case 1:
        h ^= data[0];
        h *= 0x5bd1e995;
    }

    h ^= h >> 13;
    h *= 0x5bd1e995;
    h ^= h >> 15;

    return h;
}


uint64_t ztl_murmur_hash2_64(const void* key, uint32_t len, uint64_t seed)
{
    const uint64_t m = 0xc6a4a7935bd1e995LLU;
    const int r = 47;

    uint64_t h = seed ^ (len * m);

    const uint64_t * data = (const uint64_t *)key;
    const uint64_t * end = data + (len / 8);

    while (data != end)
    {
        uint64_t k = *data++;

        k *= m;
        k ^= k >> r;
        k *= m;

        h ^= k;
        h *= m;
    }

    const unsigned char * data2 = (const unsigned char*)data;

    switch (len & 7)
    {
    case 7: h ^= (uint64_t)(data2[6]) << 48;
    case 6: h ^= (uint64_t)(data2[5]) << 40;
    case 5: h ^= (uint64_t)(data2[4]) << 32;
    case 4: h ^= (uint64_t)(data2[3]) << 24;
    case 3: h ^= (uint64_t)(data2[2]) << 16;
    case 2: h ^= (uint64_t)(data2[1]) << 8;
    case 1: h ^= (uint64_t)(data2[0]);
        h *= m;
    };

    h ^= h >> r;
    h *= m;
    h ^= h >> r;

    return h;
}


uint32_t ztl_hashpjw(const void* key)
{
    const char* ptr = key;
    uint32_t val = 0, tmp;

    while (*ptr != '\0')
    {
        val = (val << 4) + *ptr++;
        if ((tmp = val & (unsigned int)0xf0000000) != 0)
            val = (val ^ (tmp >> 24)) ^ tmp;
    }
    return val;
}


uint32_t ztl_hashdjb2(const void* key)
{
    const char *ptr = key;
    int c;
    unsigned hash = 5381;

    while ((c = *ptr++))
        /* hash * 33 + c */
        hash = ((hash << 5) + hash) + c;
    return hash;
}
